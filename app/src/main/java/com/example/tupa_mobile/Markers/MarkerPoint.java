package com.example.tupa_mobile.Markers;

public class MarkerPoint {
    private String id;
    private double count, latitude, longitude;

    public String getId() {
        return id;
    }

    public double getCount() {
        return count;
    }

    public double getLatitude() {
        return latitude;
    }

    public double getLongitude() {
        return longitude;
    }
}
